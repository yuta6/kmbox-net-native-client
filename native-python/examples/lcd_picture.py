def rgb565_bytes() -> bytes:
    data = base64.b64decode("".join(B64_ZLIB_RGB565_128X160.split()))
    raw = zlib.decompress(data)
    expected = 128 * 160 * 2
    if len(raw) != expected:
        raise ValueError(f"unexpected payload size: {len(raw)} != {expected}")
